class DiskSpace(object):

    def minDrives(self, used, total):

        total = list(total)
        total.sort(reverse=True)
        used_tot = 0
        used_drives = 0

        for x in used:
            used_tot += x

        for i in total:
            if used_tot > 0:
                used_drives += 1
                used_tot -= i

        return used_drives






